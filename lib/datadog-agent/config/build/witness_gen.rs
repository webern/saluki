//! Build-time codegen for the Datadog configuration witness trait and driver.
//!
//! For every overlay key marked `support: full` or `support: partial`, this emits one
//! `consume_<key>` method on the `DatadogConfigConsumer` trait plus a line in `drive()` that reads
//! the corresponding value out of a [`DatadogConfiguration`] and hands it to the consumer. The trait
//! has no default method bodies, so the hand-written translator that implements it *must* account
//! for every supported key — a supported key without a destination in the translator is a compile
//! error, and an orphaned method left after a key is removed is also a compile error.
//!
//! The value type for each method mirrors the *generated struct's* field type, which follows the
//! schema's declared `type` (so a schema `number` surfaces as `f64`). Semantic refinement — clamping
//! a port `f64` to a `u16`, mapping a string to an enum — is the translator's job, not the witness's.
//!
//! The driver reads values by serializing the configuration to a `serde_json::Value` once and
//! navigating each key's dotted path. This keeps the generator independent of typify's exact field
//! identifiers and nesting, and the values are already schema-defaulted because the configuration was
//! deserialized with `#[serde(default = ...)]` in place.

use std::path::Path;

use datadog_agent_config_overlay_model::schema_gen::{FieldInfo, FieldType};
use datadog_agent_config_overlay_model::{KnownEntry, SchemaOverlay};
use indexmap::IndexMap;

pub fn generate(overlay: &SchemaOverlay, schema_map: &IndexMap<String, FieldInfo>, manifest_dir: &Path) {
    let mut keys: Vec<&String> = overlay
        .inventory
        .iter()
        .filter(|(_, entry)| matches!(entry, KnownEntry::Full(_) | KnownEntry::Partial(_)))
        .map(|(key, _)| key)
        .collect();
    keys.sort();

    let mut trait_methods = String::new();
    let mut drive_calls = String::new();

    for key in &keys {
        let method = sanitize(key);
        let ty = value_type(schema_map.get(*key).map(|info| &info.value_type));
        trait_methods.push_str(&format!(
            "    /// Consume the `{key}` value.\n    fn consume_{method}(&mut self, value: {ty});\n\n"
        ));
        drive_calls.push_str(&format!(
            "    consumer.consume_{method}(witness_get(&root, \"{key}\"));\n"
        ));
    }

    let mut out = String::new();
    out.push_str("// @generated by build.rs from schema_overlay.yaml — DO NOT EDIT\n");
    out.push_str("// Regenerate by running `cargo build -p datadog-agent-config`.\n");
    out.push_str("#![allow(clippy::all, dead_code, unused)]\n\n");
    out.push_str("use serde::de::DeserializeOwned;\n\n");
    out.push_str("use crate::datadog_configuration::DatadogConfiguration;\n\n");

    out.push_str(WITNESS_DOC);
    out.push_str("pub trait DatadogConfigConsumer {\n");
    out.push_str(&trait_methods);
    out.push_str("}\n\n");

    out.push_str(DRIVE_DOC);
    out.push_str("pub fn drive(config: &DatadogConfiguration, consumer: &mut impl DatadogConfigConsumer) {\n");
    out.push_str("    let root = serde_json::to_value(config).expect(\"DatadogConfiguration always serializes\");\n");
    out.push_str(&drive_calls);
    out.push_str("}\n\n");

    out.push_str(HELPER);

    let path = manifest_dir.join("src/witness.rs");
    std::fs::write(&path, out).unwrap_or_else(|e| panic!("cannot write {}: {}", path.display(), e));

    let _ = std::process::Command::new("rustfmt")
        .arg("--edition")
        .arg("2021")
        .arg(&path)
        .status();
}

/// Map a schema field type to the Rust type the witness method receives.
///
/// These mirror typify's mapping for the generated struct so the serialized value deserializes
/// cleanly: `integer -> i64`, `number -> f64`, `boolean -> bool`, string list -> `Vec<String>`. A
/// scalar string is taken as `Option<String>` so an absent value is `None` rather than an error, and
/// anything the schema does not type is taken as a raw `serde_json::Value`.
fn value_type(field_type: Option<&FieldType>) -> &'static str {
    match field_type {
        Some(FieldType::Bool) => "bool",
        Some(FieldType::Integer) => "i64",
        Some(FieldType::Float) => "f64",
        Some(FieldType::String) => "Option<String>",
        Some(FieldType::StringList) => "Vec<String>",
        Some(FieldType::Unknown) | None => "serde_json::Value",
    }
}

/// Turn a dotted config key into a valid Rust identifier fragment.
fn sanitize(key: &str) -> String {
    key.chars()
        .map(|c| if c.is_ascii_alphanumeric() { c } else { '_' })
        .collect()
}

const WITNESS_DOC: &str = "\
/// Generated trait with one method per supported Datadog configuration key.
///
/// The hand-written translator implements this trait. Because there are no default method bodies,
/// the compiler forces the translator to give every supported key an explicit destination.
";

const DRIVE_DOC: &str = "\
/// Drive a consumer over every supported key in a [`DatadogConfiguration`].
";

const HELPER: &str = "\
/// Navigate `root` by the dotted `path` and deserialize the value into `T`.
///
/// A missing or null value yields `T::default()`. A value that fails to deserialize also yields the
/// default; the generated value types are chosen to match the serialized representation, so this only
/// trips on genuinely malformed input.
fn witness_get<T: DeserializeOwned + Default>(root: &serde_json::Value, path: &str) -> T {
    let mut cur = root;
    for part in path.split('.') {
        match cur.get(part) {
            Some(value) => cur = value,
            None => return T::default(),
        }
    }
    if cur.is_null() {
        return T::default();
    }
    serde_json::from_value(cur.clone()).unwrap_or_default()
}
";
