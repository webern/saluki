//! Build-time codegen for the Datadog config witness trait and its driver.
//!
//! The witness trait makes supported-key coverage a compile-time guarantee: it declares one
//! `consume_<key>` method per `support: full` / `support: partial` overlay leaf, and a `drive`
//! function that feeds every leaf's already-defaulted value to a consumer. A hand-written
//! `Translator` implements the trait; if a supported key is added or removed, the trait changes and
//! the hand-written impl fails to compile until a human handles the new shape.
//!
//! Method types are read verbatim from the shared `typify` AST (`TypifiedSchema::file`) that also
//! produces `datadog_configuration.rs`, so a witness method's parameter type can never drift from
//! the generated `DatadogConfiguration` field it mirrors.
use std::collections::BTreeMap;
use std::fmt::Write as _;
use std::path::Path;

use syn::visit_mut::{self, VisitMut};
use syn::{Fields, Item, Type};

use crate::datadog_config_gen::TypifiedSchema;

/// The Rust type token (already path-shortened) for a leaf, plus the dotted overlay path.
struct Leaf {
    /// Dotted overlay path, for example `apm_config.obfuscation.credit_cards.enabled`.
    dotted: String,
    /// The leaf field's Rust type, rendered to a string (for example `f64`, `Option<String>`).
    ty: String,
    /// Whether the leaf type is `Copy` (so `drive` can pass it by value without `.clone()`).
    is_copy: bool,
    /// The chain of struct field idents from the root to this leaf (one per path segment).
    segments: Vec<String>,
}

/// A struct in the typify AST: maps each field ident to its declared type.
struct StructInfo {
    fields: BTreeMap<String, Type>,
}

pub fn generate(
    overlay: &datadog_agent_config_overlay_model::SchemaOverlay, typified: &TypifiedSchema, manifest_dir: &Path,
) {
    let structs = collect_structs(&typified.file);

    // Walk the supported keys in overlay declaration order so the generated trait and driver read
    // in the same order a human edits the overlay.
    let mut leaves: Vec<Leaf> = Vec::new();
    for (dotted, entry) in &overlay.inventory {
        if !matches!(
            entry,
            datadog_agent_config_overlay_model::KnownEntry::Full(_)
                | datadog_agent_config_overlay_model::KnownEntry::Partial(_)
        ) {
            continue;
        }
        leaves.push(resolve_leaf(dotted, &structs));
    }

    // Cross-check the witness against the shared prune+typify supported set: the two must describe
    // exactly the same keys, or one of the generators is operating on a stale view.
    assert_eq!(
        leaves.len(),
        typified.supported.len(),
        "witness leaf count ({}) disagrees with typify supported set ({})",
        leaves.len(),
        typified.supported.len()
    );
    for leaf in &leaves {
        assert!(
            typified.supported.contains(&leaf.dotted),
            "witness key '{}' is absent from the typify supported set",
            leaf.dotted
        );
    }

    let body = render(&leaves);

    let mut out = String::new();
    out.push_str("// @generated by build.rs from core_schema.yaml + schema_overlay.yaml — DO NOT EDIT\n");
    out.push_str("// Regenerate by running `cargo build -p datadog-agent-config`.\n");
    out.push_str("#![allow(clippy::all, dead_code, unused)]\n\n");
    out.push_str(&body);

    let path = manifest_dir.join("src/witness.rs");
    std::fs::write(&path, out).unwrap_or_else(|e| panic!("cannot write {}: {}", path.display(), e));

    // Canonicalize to the repo's rustfmt style so the committed @generated file is stable across
    // regenerations. Best-effort: a missing rustfmt is not fatal.
    let _ = std::process::Command::new("rustfmt")
        .arg("--edition")
        .arg("2021")
        .arg(&path)
        .status();
}

/// Build a name -> `StructInfo` map for every struct typify emitted.
fn collect_structs(file: &syn::File) -> BTreeMap<String, StructInfo> {
    let mut out = BTreeMap::new();
    for item in &file.items {
        if let Item::Struct(s) = item {
            let mut fields = BTreeMap::new();
            if let Fields::Named(named) = &s.fields {
                for field in &named.named {
                    if let Some(ident) = &field.ident {
                        fields.insert(ident.to_string(), field.ty.clone());
                    }
                }
            }
            out.insert(s.ident.to_string(), StructInfo { fields });
        }
    }
    out
}

/// Descend the struct tree from the root following a dotted path, returning the leaf's type.
///
/// Each non-final segment must resolve to a (possibly `Option`-wrapped) named struct that the next
/// segment lives in; the final segment's field type is captured verbatim and path-shortened.
fn resolve_leaf(dotted: &str, structs: &BTreeMap<String, StructInfo>) -> Leaf {
    let segments: Vec<&str> = dotted.split('.').collect();
    let mut current = "DatadogConfiguration".to_string();

    for (i, segment) in segments.iter().enumerate() {
        let info = structs
            .get(&current)
            .unwrap_or_else(|| panic!("witness: struct '{}' not found while resolving '{}'", current, dotted));
        let field_ty = info.fields.get(*segment).unwrap_or_else(|| {
            panic!(
                "witness: field '{}' not found in struct '{}' (path '{}')",
                segment, current, dotted
            )
        });

        if i == segments.len() - 1 {
            let shortened = shorten_type(field_ty.clone());
            return Leaf {
                dotted: dotted.to_string(),
                ty: type_to_string(&shortened),
                is_copy: is_copy_type(&shortened),
                segments: segments.iter().map(|s| s.to_string()).collect(),
            };
        }

        // Intermediate segment: unwrap any `Option<...>` and descend into the named struct.
        current = inner_struct_name(field_ty).unwrap_or_else(|| {
            panic!(
                "witness: segment '{}' of '{}' is not a (boxed/optional) named struct",
                segment, dotted
            )
        });
    }

    unreachable!("a non-empty dotted path always returns from the loop")
}

/// Extract the named-struct identifier from a section field type, unwrapping `Option`/`Box`.
fn inner_struct_name(ty: &Type) -> Option<String> {
    match ty {
        Type::Path(tp) => {
            let last = tp.path.segments.last()?;
            let ident = last.ident.to_string();
            if ident == "Option" || ident == "Box" {
                if let syn::PathArguments::AngleBracketed(args) = &last.arguments {
                    for arg in &args.args {
                        if let syn::GenericArgument::Type(inner) = arg {
                            return inner_struct_name(inner);
                        }
                    }
                }
                None
            } else {
                Some(ident)
            }
        }
        _ => None,
    }
}

/// Render a `syn::Type` to a single-line Rust source string.
///
/// Hand-written so the build script needs no token-printing dependency (`quote`/`proc-macro2`). It
/// covers exactly the shapes typify emits for our schema: path types, optional leading `::`,
/// angle-bracketed generic arguments, and references; anything else is a build-time error.
fn type_to_string(ty: &Type) -> String {
    let mut out = String::new();
    write_type(ty, &mut out);
    out
}

fn write_type(ty: &Type, out: &mut String) {
    match ty {
        Type::Path(tp) => {
            if tp.qself.is_some() {
                panic!("witness: unexpected qualified self type in a leaf field");
            }
            if tp.path.leading_colon.is_some() {
                out.push_str("::");
            }
            for (i, seg) in tp.path.segments.iter().enumerate() {
                if i > 0 {
                    out.push_str("::");
                }
                out.push_str(&seg.ident.to_string());
                if let syn::PathArguments::AngleBracketed(args) = &seg.arguments {
                    out.push('<');
                    let mut first = true;
                    for arg in &args.args {
                        if !first {
                            out.push_str(", ");
                        }
                        first = false;
                        match arg {
                            syn::GenericArgument::Type(inner) => write_type(inner, out),
                            _ => panic!("witness: unexpected non-type generic argument in a leaf field"),
                        }
                    }
                    out.push('>');
                }
            }
        }
        Type::Reference(r) => {
            out.push('&');
            if let Some(lt) = &r.lifetime {
                out.push_str(&lt.to_string());
                out.push(' ');
            }
            if r.mutability.is_some() {
                out.push_str("mut ");
            }
            write_type(&r.elem, out);
        }
        _ => panic!("witness: unsupported leaf field type shape"),
    }
}

/// Apply the same prelude-path shortening the data model uses, so witness method types match the
/// names in scope (`String`, `Option`, `Vec`, `HashMap`) instead of typify's `::std::...` paths.
fn shorten_type(mut ty: Type) -> Type {
    let mut shortener = WitnessPathShortener;
    shortener.visit_type_mut(&mut ty);
    ty
}

/// Mirror of the data model's `PathShortener` for the prelude paths typify emits.
struct WitnessPathShortener;

impl VisitMut for WitnessPathShortener {
    fn visit_path_mut(&mut self, path: &mut syn::Path) {
        visit_mut::visit_path_mut(self, path);

        if path.leading_colon.is_none() {
            return;
        }
        let idents: Vec<String> = path.segments.iter().map(|s| s.ident.to_string()).collect();
        let segments: Vec<&str> = idents.iter().map(String::as_str).collect();
        let shorten = matches!(
            segments.as_slice(),
            ["std", "option", "Option"]
                | ["std", "string", "String"]
                | ["std", "vec", "Vec"]
                | ["std", "boxed", "Box"]
                | ["std", "collections", "HashMap"]
        );
        if shorten {
            let last = path.segments.last().cloned().expect("non-empty path");
            path.leading_colon = None;
            path.segments = std::iter::once(last).collect();
        }
    }
}

/// Whether a leaf type is `Copy` (so `drive` can pass it by value without cloning).
///
/// Conservative: only the primitive scalars typify emits for our schema are treated as `Copy`.
/// Everything else (`String`, `Vec`, `HashMap`, `Option<...>`, `serde_json::Value`) is cloned.
fn is_copy_type(ty: &Type) -> bool {
    if let Type::Path(tp) = ty {
        if tp.path.leading_colon.is_none() && tp.path.segments.len() == 1 {
            let ident = tp.path.segments[0].ident.to_string();
            return matches!(
                ident.as_str(),
                "bool"
                    | "f32"
                    | "f64"
                    | "i8"
                    | "i16"
                    | "i32"
                    | "i64"
                    | "isize"
                    | "u8"
                    | "u16"
                    | "u32"
                    | "u64"
                    | "usize"
                    | "char"
            );
        }
    }
    false
}

/// Convert a dotted overlay path to a `consume_*` method name (dots -> underscores).
fn method_name(dotted: &str) -> String {
    format!("consume_{}", dotted.replace('.', "_"))
}

/// Render the witness trait and `drive` function source.
fn render(leaves: &[Leaf]) -> String {
    let mut out = String::new();

    // Imports follow the repo's `StdExternalCrate` grouping: std first, then crate. The build
    // script formats with stable `rustfmt`, so emitting them already-grouped keeps the committed
    // file stable under `cargo +nightly fmt`.
    //
    // `additional_endpoints` (and any future map leaf) renders `HashMap<...>` in scope; bring it in
    // only when a leaf actually uses it so the generated module stays warning-clean.
    if leaves.iter().any(|l| l.ty.contains("HashMap<")) {
        out.push_str("use std::collections::HashMap;\n\n");
    }
    out.push_str("use crate::datadog_configuration::*;\n");
    out.push_str("use crate::DatadogConfiguration;\n\n");

    // The trait: one consume method per supported leaf.
    out.push_str("/// Witness over every supported Datadog configuration key.\n");
    out.push_str("///\n");
    out.push_str("/// One method per `support: full` / `support: partial` overlay leaf. A type that implements\n");
    out.push_str("/// this trait is forced by the compiler to decide a destination for every supported key: adding\n");
    out.push_str("/// or removing a key changes the trait and breaks the implementation until it is handled.\n");
    out.push_str("pub trait DatadogConfigConsumer {\n");
    for leaf in leaves {
        let _ = writeln!(out, "    /// Consume `{}`.", leaf.dotted);
        let _ = writeln!(
            out,
            "    fn {}(&mut self, value: {});",
            method_name(&leaf.dotted),
            leaf.ty
        );
    }
    out.push_str("}\n\n");

    // The driver: feeds each leaf's already-defaulted value to the consumer.
    out.push_str("/// Feed every supported key's already-defaulted value into `consumer`.\n");
    out.push_str("///\n");
    out.push_str("/// Absent sections resolve to their schema defaults via `unwrap_or_default()` chains (every\n");
    out.push_str("/// generated section struct threads the schema default functions through its `Default` impl), so\n");
    out.push_str("/// the consumer always receives a concrete, defaulted value.\n");
    out.push_str("pub fn drive(config: &DatadogConfiguration, consumer: &mut impl DatadogConfigConsumer) {\n");
    for leaf in leaves {
        let access = drive_access(leaf);
        let _ = writeln!(out, "    consumer.{}({});", method_name(&leaf.dotted), access);
    }
    out.push_str("}\n");

    out
}

/// Build the `config.<...>` access expression for a leaf in `drive`.
///
/// Top-level leaves read the field directly; nested leaves walk through each `Option<Section>` with
/// `.clone().unwrap_or_default()` so an absent section falls back to its schema default. The final
/// leaf field is cloned only when its type is not `Copy`.
fn drive_access(leaf: &Leaf) -> String {
    let mut expr = String::from("config");

    // Intermediate (section) segments: clone the section then default it if absent.
    for segment in &leaf.segments[..leaf.segments.len() - 1] {
        let _ = write!(expr, ".{}", segment);
        expr.push_str(".clone().unwrap_or_default()");
    }

    // Final (leaf) segment.
    let last = leaf.segments.last().expect("non-empty path");
    let _ = write!(expr, ".{}", last);
    if !leaf.is_copy {
        expr.push_str(".clone()");
    }
    expr
}
